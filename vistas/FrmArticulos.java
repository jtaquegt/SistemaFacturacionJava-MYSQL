    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {                                          
        // TODO add your handling code here:
        ArticuloDAO dao = new ArticuloDAO();
        Articulo articulo = null;

        String codigo = txtCodigo.getText().trim();
        String nombre = txtNombre.getText().trim();

        // Validar que al menos un campo tenga valor
        if (codigo.isEmpty() && nombre.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese el código o el nombre del articulo para buscar.", 
                                          "Campo vacío", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Buscar por código o nombre
        if (!codigo.isEmpty()) {
            articulo = dao.buscarArticuloPorCodigo(codigo);
        } 
        if (articulo == null && !nombre.isEmpty()) {
            articulo = dao.buscarPorNombre(nombre);
        }

        // Validar si se encontró el artículo
        if (articulo != null) {
            txtCodigo.setText(articulo.getCodigo());
            txtNombre.setText(articulo.getNombre());
            txtDescripcion.setText(articulo.getDescripcion());
            txtCantidad.setText(String.valueOf(articulo.getCantidad()));
            txtPrecio.setText(String.valueOf(articulo.getPrecio()));

            // Seleccionar el proveedor en el ComboBox
            for (int i = 0; i < cbxProveedor.getItemCount(); i++) {
                ProveedorItem p = cbxProveedor.getItemAt(i);
                if (p.getIdProveedor() == articulo.getIdProveedor()) {
                    cbxProveedor.setSelectedIndex(i);
                    break;
                }
            }

        } else {
            JOptionPane.showMessageDialog(this, "No se encontro el articulo.", 
                                          "No encontrado", JOptionPane.INFORMATION_MESSAGE);
            limpiarCampos();
        }
    }                                         

