    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {                                            
        // TODO add your handling code here:
        ArticuloDAO dao = new ArticuloDAO();

        String codigo = txtCodigo.getText().trim();
        String nombre = txtNombre.getText().trim();

        // Validar que al menos haya un identificador
        if (codigo.isEmpty() && nombre.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese el codigo o nombre del articulo para eliminar.", 
                                          "Campo vacio", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Confirmar eliminación
        int opcion = JOptionPane.showConfirmDialog(this, 
                        "¿Esta seguro que desea eliminar este artículo?", 
                        "Confirmar eliminación", JOptionPane.YES_NO_OPTION);
        if (opcion != JOptionPane.YES_OPTION) {
            return; // Si el usuario cancela, no hacemos nada
        }

        boolean eliminado = false;

        // Intentar eliminar por código si está disponible
        if (!codigo.isEmpty()) {
            eliminado = dao.eliminarPorCodigo(codigo);
        }

        // Si no se eliminó por código, intentar por nombre
        if (!eliminado && !nombre.isEmpty()) {
            eliminado = dao.eliminarPorNombre(nombre);
        }

        // Mensajes según el resultado
        if (eliminado) {
            JOptionPane.showMessageDialog(this, "Articulo eliminado correctamente.", 
                                          "Exito", JOptionPane.INFORMATION_MESSAGE);
            limpiarCampos();
            listarArticulos(); // Refresca la tabla
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo eliminar el artículo o no existe.", 
                                          "Error", JOptionPane.ERROR_MESSAGE);
        }
    }                                           

