    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {                                           
        // TODO add your handling code here:
        ArticuloDAO dao = new ArticuloDAO();
        Articulo articulo = new Articulo();

        //Validar campos vacíos
        if (txtCodigo.getText().trim().isEmpty() ||
            cbxProveedor.getSelectedIndex() == -1 ||
            txtNombre.getText().trim().isEmpty() ||
            txtDescripcion.getText().trim().isEmpty() ||
            txtCantidad.getText().trim().isEmpty() ||
            txtPrecio.getText().trim().isEmpty()) {

            JOptionPane.showMessageDialog(this, "Por favor complete todos los campos.", 
                                          "Campos vacíos", JOptionPane.WARNING_MESSAGE);
            return;
        }

        //Validar tipo de dato numerico
        int cantidad;
        double precio;
        try {
            cantidad = Integer.parseInt(txtCantidad.getText().trim());
            precio = Double.parseDouble(txtPrecio.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Cantidad y precio deben ser valores numéricos.", 
                                          "Error de formato", JOptionPane.ERROR_MESSAGE);
            return;
        }

        //Obtener el proveedor seleccionado
        ProveedorItem proveedorSeleccionado = (ProveedorItem) cbxProveedor.getSelectedItem();
        int idProveedor = proveedorSeleccionado.getIdProveedor();

        //Validar que el proveedor exista en BD
        if (!dao.existeProveedor(idProveedor)) {
            JOptionPane.showMessageDialog(this, "El proveedor seleccionado no existe.", 
                                          "Proveedor inválido", JOptionPane.ERROR_MESSAGE);
            return;
        }

        //Validar ducplicidad por codigo o nombre
        if (dao.buscarArticuloPorCodigo(txtCodigo.getText().trim()) != null) {
            JOptionPane.showMessageDialog(this, "Ya existe un artículo con ese código.", 
                                          "Duplicado", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (dao.buscarPorNombre(txtNombre.getText().trim()) != null) {
            JOptionPane.showMessageDialog(this, "Ya existe un artículo con ese nombre.", 
                                          "Duplicado", JOptionPane.WARNING_MESSAGE);
            return;
        }

        //Asignar Valores al modelo
        articulo.setCodigo(txtCodigo.getText().trim());
        articulo.setIdProveedor(idProveedor);
        articulo.setNombre(txtNombre.getText().trim());
        articulo.setDescripcion(txtDescripcion.getText().trim());
        articulo.setCantidad(cantidad);
        articulo.setPrecio(precio);

        //Guardar el Articulo usando DAO
        if (dao.agregarArticulo(articulo)) {
            JOptionPane.showMessageDialog(this, "Artículo registrado correctamente.", 
                                          "Éxito", JOptionPane.INFORMATION_MESSAGE);
            limpiarCampos();
            listarArticulos(); // Refrescar la tabla
        } else {
            JOptionPane.showMessageDialog(this, "Error al guardar el artículo.", 
                                          "Error", JOptionPane.ERROR_MESSAGE);
        }
       
    }                                          

